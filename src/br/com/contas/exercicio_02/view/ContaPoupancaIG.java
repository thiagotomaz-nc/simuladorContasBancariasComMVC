    private void jLabel1AncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_jLabel1AncestorAdded
        if (cp != null) {
            setTitle(getTitle() + "  - Editar Conta poupança");
            txtNumeroDaConta.setText(cp.getNumeroConta().trim());
            txtNumeroDaConta.setEnabled(false);
            txtNomeContaPoupanca.setText(cp.getNome());
            txtSaldoContaPoupanca.setText(formatoMoedaBrasil.format(cp.getSaldo()));
            txtSaldoContaPoupaca.setText(formatoMoedaBrasil.format(cp.getSaldoPoupanca()));
            btnCadatrarEditar.setText("Atualizar Conta");
            cp = null;
        }
    }//GEN-LAST:event_jLabel1AncestorAdded

