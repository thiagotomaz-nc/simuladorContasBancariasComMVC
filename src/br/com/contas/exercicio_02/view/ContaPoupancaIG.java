    private void btnCadatrarEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadatrarEditarActionPerformed

        if (!txtNomeContaPoupanca.getText().trim().toUpperCase().isEmpty() && !txtSaldoContaPoupanca.getText().trim().isEmpty() && !txtSaldoContaPoupaca.getText().trim().isEmpty()) {
            if (txtNumeroDaConta.getText().trim().length() == 8) {
                double saldo = Double.parseDouble(txtSaldoContaPoupanca.getText().trim().replace("R$", "").replace(" ","").replace(".", "").replace(",", "."));
                double saldoDaPoupanca = Double.parseDouble(txtSaldoContaPoupaca.getText().trim().replace("R$", "").replace(" ","").replace(".", "").replace(",", "."));

                if (saldo==0) saldo=0;
                if (saldoDaPoupanca==0) saldoDaPoupanca = 0;
                
                if (saldo >= 0 && saldoDaPoupanca >= 0) {
                    cp = new ContaPoupanca();
                    cp.setNumeroConta(txtNumeroDaConta.getText().trim());
                    cp.setNome(txtNomeContaPoupanca.getText().trim().toUpperCase());
                    cp.setSaldo(saldo);
                    cp.setSaldoPoupanca(saldoDaPoupanca);
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Por favor, preencha o saldo e/ou o saldo da poupança com valor a partir de R$ 0 reais!");

                }
            } else {
                JOptionPane.showMessageDialog(this, "O campo numero da conta tem que estar completamente preenchido");
                txtNumeroDaConta.setBorder(new LineBorder(Color.RED, 2));
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, verifique se todos os campos estão preenchidos!");

        }


    }//GEN-LAST:event_btnCadatrarEditarActionPerformed

